    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        String dato = JOptionPane.showInputDialog("Entre el placa del carro:");
        try {
            String plac = (dato);

            OperacionesBaseDatos.conectarBaseDatos();
            OperacionesBaseDatos.borrarCarros(plac);
            OperacionesBaseDatos.desconectarBD();

            JOptionPane.showMessageDialog(null, "Carro eliminado!");
            mostrar();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

