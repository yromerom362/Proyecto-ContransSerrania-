   private void mostrar() {
        try {
            OperacionesBaseDatos.conectarBaseDatos();
            ResultSet rs = OperacionesBaseDatos.obtenerCarros();

            carros.setText("");  // Borra el contenido
            while (rs.next()) {   // Recorremos la Base de datos
                String placa = rs.getString("placa");
                String tipo_carros = rs.getString("tipo_carros");

                carros.append(placa + " - " + tipo_carros + "\n");
            }
            OperacionesBaseDatos.desconectarBD();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
